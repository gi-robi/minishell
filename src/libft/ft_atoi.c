#include "libft.h"
#include <ctype.h>
#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	signo;

	i = 0;
	n = 0;
	signo = 1;
	while (str[i] == '\f' || str[i] == '\n' || str[i] == '\r' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == ' ')
	{
		i++;
	}
	if (str[i] == '-')
	{
		signo = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * signo);
}
