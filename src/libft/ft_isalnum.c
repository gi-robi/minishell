#include "libft.h"
#include <stdio.h>

int	ft_isalnum(char c)
{
	if ((c >= '0') && (c <= '9'))
	{
		return (1);
	}
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
