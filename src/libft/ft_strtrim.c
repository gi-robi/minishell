#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	j;
	char	*frase;

	i = 0;
	if (!s1 || !set)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	j = ft_strlen((char *)s1);
	while (j > i && ft_strchr(set, s1[j - 1]))
		j--;
	frase = ft_substr(s1, i, j - i);
	return (frase);
}
